package de.htwg.ticketqualitymonitor.model;

public class JiraProject {

	private String id;
	private String key;
	private String name;
	private String description;

	public JiraProject() {
	}

	public String getId() {
		return id;
	}

	public String getKey() {
		return key;
	}

	public String getName() {
		return name;
	}

	public String getDescription() {
		return description;
	}

}
